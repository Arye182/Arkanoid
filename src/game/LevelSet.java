package game;

/**
 * The type Level set.
 */
public class LevelSet {
    private String key;
    private String message;
    private String path;

    /**
     * Gets key.
     *
     * @return the key
     */
    public String getKey() {
        return key;
    }

    /**
     * Gets message.
     *
     * @return the message
     */
    public String getMessage() {
        return message;
    }

    /**
     * Gets path.
     *
     * @return the path
     */
    public String getPath() {
        return path;
    }

    /**
     * Sets key.
     *
     * @param k the key
     */
    public void setKey(String k) {
        this.key = k;
    }

    /**
     * Sets message.
     *
     * @param m the message
     */
    public void setMessage(String m) {
        this.message = m;
    }

    /**
     * Sets path.
     *
     * @param p the path
     */
    public void setPath(String p) {
        this.path = p;
    }

}
